#!/usr/bin/env bash
# Smoke test: OMX project-scoped setup is reproducible from vendored source.
#
# Checks:
#   1. vendors/oh-my-codex/dist/cli/omx.js exists (vendored build present)
#   2. tools/omx/setup-project.sh exists (setup wrapper present)
#   3. Running setup in a temp repo fixture generates .codex/ with a config
#      that references the vendored OMX path, not /tmp or a user-home path.
#
# Exit 0 = all checks pass.  Exit 1 = at least one check failed.

set -euo pipefail

REPO_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/../.." && pwd)"
PASS=0
FAIL=0

ok()   { echo "  [ok]  $*"; ((PASS++)) || true; }
fail() { echo "  [FAIL] $*"; ((FAIL++)) || true; }

echo "=== omx-project-setup smoke test ==="

# ── Check 1: vendored OMX build ───────────────────────────────────────────────
OMX_JS="$REPO_ROOT/vendors/oh-my-codex/dist/cli/omx.js"
if [[ -f "$OMX_JS" ]]; then
    ok "vendors/oh-my-codex/dist/cli/omx.js exists"
else
    fail "vendors/oh-my-codex/dist/cli/omx.js missing — vendor OMX first"
fi

# ── Check 2: setup wrapper ────────────────────────────────────────────────────
WRAPPER="$REPO_ROOT/tools/omx/setup-project.sh"
if [[ -f "$WRAPPER" ]]; then
    ok "tools/omx/setup-project.sh exists"
else
    fail "tools/omx/setup-project.sh missing"
fi

# ── Check 3: setup produces repo-local config in a temp fixture ───────────────
if [[ -f "$WRAPPER" ]]; then
    TMP_REPO="$(mktemp -d)"
    trap 'rm -rf "$TMP_REPO"' EXIT

    # Run the wrapper in the temp dir
    cd "$TMP_REPO"
    bash "$WRAPPER" --repo-root "$REPO_ROOT" --target-dir "$TMP_REPO" >/dev/null 2>&1 || true

    CONFIG_FILE="$TMP_REPO/.omx/setup-scope.json"
    if [[ -f "$CONFIG_FILE" ]]; then
        ok ".omx/setup-scope.json generated"

        # Verify it references the vendored path, not /tmp or HOME
        if grep -q "$REPO_ROOT/vendors/oh-my-codex" "$CONFIG_FILE"; then
            ok "setup-scope.json references vendored OMX path"
        else
            fail "setup-scope.json does not reference vendored OMX path: $(cat "$CONFIG_FILE")"
        fi

        # Verify omx_vendor_path specifically is repo-local (not /tmp or HOME-only)
        OMX_VENDOR_VAL="$(python3 -c "import json,sys; d=json.load(open('$CONFIG_FILE')); print(d.get('omx_vendor_path',''))" 2>/dev/null || echo "")"
        if [[ "$OMX_VENDOR_VAL" == *"/tmp"* ]] || [[ "$OMX_VENDOR_VAL" == "$HOME" ]]; then
            fail "omx_vendor_path leaked a tmp/bare-home path: $OMX_VENDOR_VAL"
        else
            ok "omx_vendor_path is not a tmp/bare-home path: $OMX_VENDOR_VAL"
        fi
    else
        fail ".omx/setup-scope.json not generated by setup-project.sh"
    fi

    cd "$REPO_ROOT"
fi

# ── Summary ───────────────────────────────────────────────────────────────────
echo ""
echo "Passed: $PASS  Failed: $FAIL"
if [[ $FAIL -gt 0 ]]; then
    exit 1
fi
exit 0
