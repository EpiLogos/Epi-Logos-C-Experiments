#!/usr/bin/env python3
"""
build_clock_degree_lut.py — Generate CLOCK_DEGREE_LUT[360] C source.

Queries Neo4j for degree-node data (hexagram, decan, ruling planet, etc.).
Falls back to computed values if Neo4j is unavailable.

Usage:
    python3 tools/build_clock_degree_lut.py > epi-lib/src/m3_clock_lut.c
    # or with Neo4j:
    NEO4J_URI=bolt://localhost:7687 NEO4J_PASSWORD=secret \\
        python3 tools/build_clock_degree_lut.py > epi-lib/src/m3_clock_lut.c

Dataset coordinates (canonical, from 00-canonical-invariants.md):
    Hexagrams: #3-1-0-{0..63}
    Decans: #2-3 (36 nodes, primary + shadow)
    Spanda stages: #1-3-{0..5}
"""
import os
import sys

# ── Canonical static data (fallback when Neo4j unavailable) ──────────────────

# Chaldean decan rulers, canonical mod-10 indices (Sun=0..Saturn=6)
# [Aries d0, Aries d1, Aries d2, Taurus d0, ...]
DECAN_RULER_TABLE = [
    4, 0, 3,  # Aries:       Mars, Sun, Venus
    2, 1, 6,  # Taurus:      Mercury, Moon, Saturn
    5, 4, 0,  # Gemini:      Jupiter, Mars, Sun
    3, 2, 1,  # Cancer:      Venus, Mercury, Moon
    6, 5, 4,  # Leo:         Saturn, Jupiter, Mars
    0, 3, 2,  # Virgo:       Sun, Venus, Mercury
    1, 6, 5,  # Libra:       Moon, Saturn, Jupiter
    4, 0, 3,  # Scorpio:     Mars, Sun, Venus
    2, 1, 6,  # Sagittarius: Mercury, Moon, Saturn
    5, 4, 0,  # Capricorn:   Jupiter, Mars, Sun
    3, 2, 1,  # Aquarius:    Venus, Mercury, Moon
    6, 5, 4,  # Pisces:      Saturn, Jupiter, Mars
]

# Planet → Chakra mapping (canonical, from medicine.rs PLANET_CHAKRA)
# Sun=6(Crown), Moon=5(ThirdEye), Mercury=4(Throat), Venus=3(Heart),
# Mars=2(Solar), Jupiter=1(Sacral), Saturn=0(Root), Uranus=7, Neptune=7, Pluto=0
PLANET_CHAKRA = [6, 5, 4, 3, 2, 1, 0, 7, 7, 0]

# Hexagram sequence per decan (approximate: 64 hexagrams / 36 decans)
# Each decan gets ~1.78 hexagrams; we do a linear mapping
def decan_to_hex(decan_idx):
    return int(decan_idx * 64 // 36)

def compute_lut_row(degree):
    """Compute a single Clock_Degree_Entry from degree (0-359) using pure math."""
    decan_idx = degree // 10           # 0-35
    zodiac_sign = degree // 30         # 0-11
    ruling_planet = DECAN_RULER_TABLE[decan_idx]
    chakra_id = PLANET_CHAKRA[ruling_planet]
    strand = 1 if degree >= 180 else 0
    hexagram_id = decan_to_hex(decan_idx)
    exact_720 = degree * 2.0
    return {
        'degree': degree,
        'hexagram_id': hexagram_id,
        'decan_idx': decan_idx,
        'ruling_planet': ruling_planet,
        'zodiac_sign': zodiac_sign,
        'chakra_id': chakra_id,
        'strand': strand,
        'exact_720': exact_720,
    }

def try_neo4j_query():
    """Try to query Neo4j for real hexagram assignments. Returns dict or None."""
    uri = os.environ.get('NEO4J_URI')
    password = os.environ.get('NEO4J_PASSWORD', 'password')
    if not uri:
        return None
    try:
        from neo4j import GraphDatabase
        driver = GraphDatabase.driver(uri, auth=('neo4j', password))
        # Query: get degree nodes with hexagram assignments
        # Canonical dataset coordinate: #3-1-0-{0..63} for hexagrams
        # Degree nodes are at degree-level in the M3 dataset
        query = """
        MATCH (d:DegreeNode)-[:HAS_HEXAGRAM]->(h:Hexagram)
        RETURN d.degree AS degree, h.hexagram_id AS hex_id
        ORDER BY d.degree
        """
        hex_by_degree = {}
        with driver.session() as session:
            for record in session.run(query):
                hex_by_degree[int(record['degree'])] = int(record['hex_id'])
        driver.close()
        return hex_by_degree if hex_by_degree else None
    except Exception as e:
        print(f"// Neo4j query failed: {e}", file=sys.stderr)
        return None

def generate_c_source(entries):
    lines = [
        '/* m3_clock_lut.c — CLOCK_DEGREE_LUT',
        ' *',
        ' * AUTO-GENERATED by tools/build_clock_degree_lut.py',
        ' * Do not edit by hand. Run `make lut` to regenerate.',
        ' *',
        ' * Source: docs/plans/CLOCK-AND-NARA-SPECS/09-cosmic-clock-plugin-tui-spec.md',
        ' */',
        '',
        '#include "m3.h"',
        '',
        'const Clock_Degree_Entry CLOCK_DEGREE_LUT[360] = {',
    ]
    for e in entries:
        line = (
            f'    {{ {e["degree"]:3d}, {e["hexagram_id"]:2d}, {e["decan_idx"]:2d}, '
            f'{e["ruling_planet"]}, {e["zodiac_sign"]:2d}, {e["chakra_id"]}, '
            f'{e["strand"]}, {e["exact_720"]:.1f}f }}, '
            f'/* deg={e["degree"]:3d} decan={e["decan_idx"]:2d} */'
        )
        lines.append(line)
    lines.append('};')
    return '\n'.join(lines) + '\n'

def main():
    # Try Neo4j first, fall back to computed
    neo4j_hexes = try_neo4j_query()

    entries = []
    for degree in range(360):
        e = compute_lut_row(degree)
        # Override hexagram with Neo4j data if available
        if neo4j_hexes and degree in neo4j_hexes:
            e['hexagram_id'] = neo4j_hexes[degree]
        entries.append(e)

    print(generate_c_source(entries))

if __name__ == '__main__':
    main()
