use std::collections::BTreeMap;
use std::path::{Path, PathBuf};
use std::process::Command;

use serde::Serialize;
use tokio::time::{sleep, Duration};

use crate::graph::client::{Neo4jClient, Neo4jConfig};
use crate::graph::doctor;
use crate::graph::redis_cache::{RedisCache, RedisConfig};
use crate::graph::semantic_cache::SemanticCacheConfig;

#[derive(Debug, Clone, Serialize)]
#[serde(rename_all = "camelCase")]
pub struct BootstrapDevReport {
    pub ok: bool,
    pub dry_run: bool,
    pub compose_file: String,
    pub redisvl_setup_script: String,
    pub env_file: String,
    pub environment: BTreeMap<String, String>,
    pub notes: Vec<String>,
}

pub fn repo_root_from_compose(compose_file: &Path) -> Result<PathBuf, String> {
    compose_file
        .parent()
        .map(Path::to_path_buf)
        .ok_or_else(|| "compose file parent not found".to_string())
}

pub fn redisvl_setup_script(repo_root: &Path) -> PathBuf {
    epi_s3_redis_context::redisvl_setup_script(repo_root)
}

pub fn env_file_path(repo_root: &Path) -> PathBuf {
    repo_root.join(".env.graph-dev")
}

pub fn bootstrap_report(
    repo_root: &Path,
    compose_file: &Path,
    dry_run: bool,
) -> BootstrapDevReport {
    let semantic_cache = SemanticCacheConfig::for_local_dev(repo_root);
    let mut environment = semantic_cache.environment_contract();
    environment.insert(
        "EPILOGOS_NEO4J_URI".into(),
        std::env::var("EPILOGOS_NEO4J_URI").unwrap_or_else(|_| "bolt://127.0.0.1:7687".into()),
    );
    environment.insert(
        "EPILOGOS_NEO4J_USER".into(),
        std::env::var("EPILOGOS_NEO4J_USER").unwrap_or_else(|_| "neo4j".into()),
    );
    environment.insert(
        "EPILOGOS_NEO4J_PASSWORD".into(),
        std::env::var("EPILOGOS_NEO4J_PASSWORD").unwrap_or_default(),
    );
    environment.insert(
        "EPILOGOS_REDIS_URI".into(),
        std::env::var("EPILOGOS_REDIS_URI").unwrap_or_else(|_| "redis://127.0.0.1:6379".into()),
    );

    BootstrapDevReport {
        ok: true,
        dry_run,
        compose_file: compose_file.display().to_string(),
        redisvl_setup_script: redisvl_setup_script(repo_root).display().to_string(),
        env_file: env_file_path(repo_root).display().to_string(),
        environment,
        notes: vec![
            "Run `source .env.graph-dev` before semantic-cache retrieval flows.".into(),
            "Redis Stack is required because RedisVL needs RediSearch commands.".into(),
        ],
    }
}

pub fn write_env_file(path: &Path, environment: &BTreeMap<String, String>) -> Result<(), String> {
    let mut lines = vec![
        "# Generated by `epi graph bootstrap-dev`".to_string(),
        "# Source this file before running live semantic-cache retrieval flows.".to_string(),
    ];
    for (key, value) in environment {
        lines.push(format!("export {}={}", key, shell_escape(value)));
    }
    std::fs::write(path, format!("{}\n", lines.join("\n")))
        .map_err(|e| format!("failed to write {}: {}", path.display(), e))
}

pub async fn run_bootstrap_dev(
    repo_root: &Path,
    compose_file: &Path,
    dry_run: bool,
    json: bool,
) -> Result<String, String> {
    let report = bootstrap_report(repo_root, compose_file, dry_run);
    if dry_run {
        return render_bootstrap(&report, json);
    }

    let compose_output = Command::new("docker")
        .args(["compose", "-f"])
        .arg(compose_file)
        .args(["up", "-d"])
        .output()
        .map_err(|e| format!("docker compose failed: {}", e))?;
    if !compose_output.status.success() {
        return Err(String::from_utf8_lossy(&compose_output.stderr).into_owned());
    }

    let setup_script = redisvl_setup_script(repo_root);
    let setup_output = Command::new("bash")
        .arg(&setup_script)
        .output()
        .map_err(|e| format!("redisvl setup failed: {}", e))?;
    if !setup_output.status.success() {
        return Err(String::from_utf8_lossy(&setup_output.stderr).into_owned());
    }

    write_env_file(&env_file_path(repo_root), &report.environment)?;
    wait_for_backends().await?;

    let doctor = doctor::collect_report(repo_root).await;
    if !doctor.ok {
        return Err(format!(
            "bootstrap finished setup but doctor still reports degradation\n{}",
            doctor::render_human(&doctor)
        ));
    }

    render_bootstrap(&report, json)
}

async fn wait_for_backends() -> Result<(), String> {
    for _ in 0..30 {
        let neo4j_ready = match Neo4jClient::connect(&Neo4jConfig::from_env()) {
            Ok(client) => client.health_check().await.unwrap_or(false),
            Err(_) => false,
        };

        let redis_ready = match RedisCache::connect(&RedisConfig::from_env()).await {
            Ok(mut cache) => cache.health_check().await.unwrap_or(false),
            Err(_) => false,
        };

        if neo4j_ready && redis_ready {
            return Ok(());
        }

        sleep(Duration::from_secs(1)).await;
    }

    Err("timed out waiting for Neo4j and Redis".into())
}

fn render_bootstrap(report: &BootstrapDevReport, json: bool) -> Result<String, String> {
    if json {
        serde_json::to_string_pretty(report).map_err(|e| e.to_string())
    } else {
        Ok(format!(
            "Graph dev bootstrap{}\n  compose: {}\n  redisvl setup: {}\n  env file: {}\n  source: source {}\n  semantic cache redis: {}",
            if report.dry_run { " (dry run)" } else { "" },
            report.compose_file,
            report.redisvl_setup_script,
            report.env_file,
            report.env_file,
            report
                .environment
                .get("EPILOGOS_SEMANTIC_CACHE_REDIS_URL")
                .cloned()
                .unwrap_or_default(),
        ))
    }
}

fn shell_escape(value: &str) -> String {
    format!("'{}'", value.replace('\'', "'\"'\"'"))
}
